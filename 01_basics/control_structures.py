# Demonstrating basic control structures
x = 10

# if-else
if x > 5:
    print("x is greater than 5")
else:
    print("x is 5 or less")

# for loop
for i in range(3):
    print("Iteration", i)

# while loop
count = 0
while count < 3:
    print("Count:", count)
    count += 1